i = int(input('qual o numero você quer saber se é primo : '))
mult = 0

for count in range(2,i):
    if (i% count ==0 ):
        print('Múltiplo de',count)
        mult += 1

if (mult == 0 ):
    print('{} é Primo!'.format(i))

else:
    print('Tem',mult,'multiplos acima de 2 e abaixo de ', i)


